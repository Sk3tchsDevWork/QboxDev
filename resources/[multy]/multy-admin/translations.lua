-- Helper function to get translation
function GetTranslation(key, lang)
    lang = lang or Config.Language or 'en'
    local keys = {}
    for k in string.gmatch(key, "[^%.]+") do
        table.insert(keys, k)
    end
    
    local value = Translations[lang]
    if not value then
        value = Translations['en'] -- Fallback to English
    end
    
    for _, k in ipairs(keys) do
        if type(value) == "table" then
            value = value[k]
        else
            break
        end
    end
    
    -- If translation not found, try English
    if not value and lang ~= 'en' then
        value = Translations['en']
        for _, k in ipairs(keys) do
            if type(value) == "table" then
                value = value[k]
            else
                break
            end
        end
    end
    
    return value or key -- Return key if translation not found
end

-- Export the function
exports('GetTranslation', GetTranslation)